/*
The sequence of triangle numbers is generated by adding the natural numbers.
So the 7th triangle number would be 1 + 2 + 3 + 4 + 5 + 6 + 7 = 28. The first
ten terms would be:

    1, 3, 6, 10, 15, 21, 28, 36, 45, 55, ...

Let us list the divisors of the first seven triangle numbers:

    1: 1
    3: 1,3
    6: 1,2,3,6
   10: 1,2,5,10
   15: 1,3,5,15
   21: 1,3,7,21
   28: 1,2,4,7,14,28

We can see that 28 is the first triangle number to have over five divisors.

What is the value of the first triangle number to have over five hundred divisors?

I solved it by bruteforce but there is a much more efficient method described
here:
https://projecteuler.net/overview=012
*/

fn main() {
    let tri = euler::triangle::triangle();

    for (_, t) in tri.enumerate() {
        let divs = euler::divisors(t);

        if divs.len() > 500 {
            println!("The triangle number {} has more than 500 factors", t);
            break;
        }
    }
}
